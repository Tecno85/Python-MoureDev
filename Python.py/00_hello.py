# Consultar el tipo de dato
# 'type()', es una función integrada (incorparada o built-in) la cual nos permite saber el tipo de una variable u objeto.

print(type("Hola Python")) # Tipo 'str'
print(type(5)) # Tipo 'int'
print(type(1.5)) # Tipo 'float'
print(type(3 + 2j)) # Tipo 'complex'
print(type(True)) # Tipo 'bool'

# Nota: Las funciones integradas(incorparada o built-in), son aquellas que estan disponibles por defecto en el lenguaje sin necesidad de importar módulos adicionales. 
